-- 顧客マスタ
CREATE TABLE MDM_CUSTOMERS (
    CUSTOMER_ID      VARCHAR(100) NOT NULL PRIMARY KEY,
    CUSTOMER_CODE    VARCHAR(100) NOT NULL UNIQUE,
    CUSTOMER_NAME    VARCHAR(500) NOT NULL,
    CUSTOMER_NAME_KANA VARCHAR(500),
    CUSTOMER_NAME_EN VARCHAR(500),
    CUSTOMER_TYPE    VARCHAR(100),
    EMAIL            VARCHAR(500),
    PHONE            VARCHAR(100),
    ADDRESS          VARCHAR(1000),
    CREDIT_LIMIT     DOUBLE,
    PAYMENT_TERMS    VARCHAR(100),
    TAX_ID           VARCHAR(100),
    IS_ACTIVE        VARCHAR(10) DEFAULT '1',
    CREATED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 製品マスタ
CREATE TABLE MDM_PRODUCTS (
    PRODUCT_ID       VARCHAR(100) NOT NULL PRIMARY KEY,
    PRODUCT_CODE     VARCHAR(100) NOT NULL UNIQUE,
    PRODUCT_NAME     VARCHAR(500) NOT NULL,
    PRODUCT_TYPE     VARCHAR(100),
    UNIT             VARCHAR(50),
    UNIT_PRICE       DOUBLE,
    DESCRIPTION      VARCHAR(2000),
    IS_ACTIVE        VARCHAR(10) DEFAULT '1',
    CREATED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- サプライヤーマスタ
CREATE TABLE MDM_SUPPLIERS (
    SUPPLIER_ID       VARCHAR(100) NOT NULL PRIMARY KEY,
    SUPPLIER_NAME     VARCHAR(500) NOT NULL,
    SUPPLIER_NAME_KANA VARCHAR(500),
    SUPPLIER_NAME_EN  VARCHAR(500),
    SUPPLIER_TYPE     VARCHAR(100) NOT NULL,
    ADDRESS           VARCHAR(1000),
    COUNTRY_CODE      VARCHAR(10),
    PHONE             VARCHAR(100),
    EMAIL             VARCHAR(500),
    PAYMENT_TERMS     VARCHAR(100),
    LEAD_TIME_DAYS    INTEGER,
    QUALITY_RATING    DOUBLE,
    DELIVERY_RATING   DOUBLE,
    IS_ACTIVE         VARCHAR(10) DEFAULT '1',
    CREATED_AT        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT        TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 組織マスタ
CREATE TABLE MDM_ORGANIZATIONS (
    ORG_ID            VARCHAR(100) NOT NULL PRIMARY KEY,
    ORG_NAME          VARCHAR(500) NOT NULL,
    ORG_TYPE          VARCHAR(100) NOT NULL,
    PARENT_ORG_ID     VARCHAR(100),
