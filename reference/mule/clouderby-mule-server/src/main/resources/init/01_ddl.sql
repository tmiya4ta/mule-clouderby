    CREATED_AT        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT        TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ============================================
-- Inventory (在庫・物流)
-- ============================================

-- 倉庫マスタ
CREATE TABLE INV_WAREHOUSES (
    WAREHOUSE_ID     INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    WAREHOUSE_CODE   VARCHAR(100) NOT NULL UNIQUE,
    WAREHOUSE_NAME   VARCHAR(500) NOT NULL,
    WAREHOUSE_TYPE   VARCHAR(100) DEFAULT 'STANDARD',
    LOCATION         VARCHAR(500),
    ADDRESS          VARCHAR(1000),
    CAPACITY         DOUBLE,
    MANAGER_ID       VARCHAR(100),
    STATUS           VARCHAR(100) DEFAULT 'ACTIVE',
    IS_ACTIVE        VARCHAR(10) DEFAULT '1',
    CREATED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT       TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 在庫
CREATE TABLE INV_INVENTORY (
    INVENTORY_ID     INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID       VARCHAR(100) NOT NULL,
    WAREHOUSE_ID     INTEGER NOT NULL,
    LOT_ID           VARCHAR(100),
    STATUS           VARCHAR(100) DEFAULT 'AVAILABLE',
    STOCK_TYPE       VARCHAR(100) DEFAULT 'NORMAL',
    LOCATION_CODE    VARCHAR(100),
    QUANTITY         DOUBLE DEFAULT 0,
    RESERVED_QTY     DOUBLE DEFAULT 0,
    UNIT             VARCHAR(50),
    IS_ACTIVE        VARCHAR(10) DEFAULT '1',
