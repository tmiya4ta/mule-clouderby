-- 材料マスタ
CREATE TABLE MDM_MATERIALS (
    MATERIAL_ID       VARCHAR(100) NOT NULL PRIMARY KEY,
    MATERIAL_NAME     VARCHAR(500) NOT NULL,
    MATERIAL_TYPE     VARCHAR(100) NOT NULL,
    UNIT              VARCHAR(50) NOT NULL,
    UNIT_PRICE        DOUBLE,
    CURRENCY          VARCHAR(10) DEFAULT 'JPY',
